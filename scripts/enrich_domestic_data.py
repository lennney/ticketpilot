def enrich_faq(item):
    domain = item.get("business_domain", "")
    extra = FAQ_ENRICH.get(domain, "")
    if extra and len(item["content"]) < 500:
        item["content"] = item["content"] + "\n\n" + extra
    return item

def enrich_policy(item):
    domain = item.get("business_domain", "")
    extra = POLICY_ENRICH.get(domain, "")
    if extra and len(item["content"]) < 500:
        item["content"] = item["content"] + "\n\n" + extra
    return item

def enrich_case(item):
    domain = item.get("business_domain", "")
    extra = CASE_ENRICH.get(domain, "")
    combined = item["issue_summary"] + "\n\n" + item["resolution"]
    if len(combined) < 500 and extra:
        item["resolution"] = item["resolution"] + "\n\n" + extra
    return item

# Load, enrich, write
faq_data = json.load(open(DATA_DIR / "faq_seed.json"))
policy_data = json.load(open(DATA_DIR / "policy_seed.json"))
case_data = json.load(open(DATA_DIR / "case_seed.json"))

faq_data = [enrich_faq(item) for item in faq_data]
policy_data = [enrich_policy(item) for item in policy_data]
case_data = [enrich_case(item) for item in case_data]

for name, data in [("faq_seed.json", faq_data), ("policy_seed.json", policy_data), ("case_seed.json", case_data)]:
    with open(DATA_DIR / name, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    lens = [len(item.get("content", item.get("issue_summary","")+"\n"+item.get("resolution",""))) for item in data]
    above_300 = sum(1 for l in lens if l > 300)
    print(f"  {name}: {len(data)} items, avg={sum(lens)//len(lens)}, >300={above_300}/{len(data)}, max={max(lens)}")

print(f"\nTotal: {len(faq_data)} FAQ + {len(policy_data)} Policy + {len(case_data)} Case = {len(faq_data)+len(policy_data)+len(case_data)}")
